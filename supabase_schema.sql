-- Replace the "Allow all for authenticated users" policy with admin-only
DROP POLICY IF EXISTS "Allow all for authenticated users" ON blocked_terms;

CREATE POLICY "Admin manage blocked_terms"
ON blocked_terms
FOR ALL
USING (
  auth.role() = 'authenticated'
  AND EXISTS (
    SELECT 1 FROM users
    WHERE users.id = auth.uid()
    AND users.role = 'administrador'
  )
)
WITH CHECK (
  auth.role() = 'authenticated'
  AND EXISTS (
    SELECT 1 FROM users
    WHERE users.id = auth.uid()
    AND users.role = 'administrador'
  )
);
