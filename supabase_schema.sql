-- ============================================================
-- Table: users
-- ============================================================
CREATE TABLE users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    correo TEXT NOT NULL UNIQUE,
    apodo TEXT NOT NULL UNIQUE,
    password_hash TEXT NOT NULL,
    role TEXT NOT NULL DEFAULT 'usuario',
    xp INTEGER NOT NULL DEFAULT 0,
    rango TEXT NOT NULL DEFAULT 'Aprendiz',
    medallas TEXT,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    fecha_registro TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_users_correo ON users(correo);
CREATE INDEX idx_users_apodo ON users(apodo);

COMMENT ON COLUMN users.role IS 'usuario or administrador';
COMMENT ON COLUMN users.medallas IS 'JSON array of medal strings';

-- ============================================================
-- Table: esquemas
-- ============================================================
CREATE TABLE esquemas (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT,
    nombre TEXT NOT NULL,
    estructura_json JSONB NOT NULL,
    dependencias_json JSONB,
    fecha_creacion TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_esquemas_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE SET NULL
);

CREATE INDEX idx_esquemas_user ON esquemas(user_id);

-- ============================================================
-- Table: validaciones
-- ============================================================
CREATE TABLE validaciones (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    esquema_id BIGINT NOT NULL,
    nivel_normalizacion TEXT NOT NULL,
    violaciones_json JSONB,
    fecha TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_validaciones_esquema FOREIGN KEY (esquema_id) REFERENCES esquemas(id) ON DELETE CASCADE
);

CREATE INDEX idx_validaciones_esquema ON validaciones(esquema_id);

-- ============================================================
-- Table: puzzles
-- ============================================================
CREATE TABLE puzzles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    enunciado TEXT NOT NULL,
    tablas_inicial JSONB NOT NULL,
    df_inicial JSONB NOT NULL,
    solucion_esperada JSONB NOT NULL,
    nivel_dificultad INTEGER DEFAULT 1,
    activo BOOLEAN DEFAULT TRUE
);

-- ============================================================
-- Table: intentos_puzzle
-- ============================================================
CREATE TABLE intentos_puzzle (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    puzzle_id BIGINT NOT NULL,
    puntuacion INTEGER NOT NULL,
    fecha TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_intentos_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_intentos_puzzle FOREIGN KEY (puzzle_id) REFERENCES puzzles(id) ON DELETE CASCADE
);

CREATE INDEX idx_intentos_user ON intentos_puzzle(user_id);
CREATE INDEX idx_intentos_puzzle_id ON intentos_puzzle(puzzle_id);

-- ============================================================
-- Table: retos_semanales
-- ============================================================
CREATE TABLE retos_semanales (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descripcion TEXT NOT NULL,
    tablas JSONB NOT NULL,
    df JSONB NOT NULL,
    fecha_inicio DATE NOT NULL,
    fecha_fin DATE NOT NULL,
    activo BOOLEAN DEFAULT TRUE,
    CONSTRAINT chk_fecha_fin CHECK (fecha_fin >= fecha_inicio)
);

-- ============================================================
-- Table: participaciones_reto
-- ============================================================
CREATE TABLE participaciones_reto (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    reto_id BIGINT NOT NULL,
    puntuacion INTEGER NOT NULL,
    tiempo_segundos INTEGER NOT NULL,
    fecha TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_participaciones_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_participaciones_reto FOREIGN KEY (reto_id) REFERENCES retos_semanales(id) ON DELETE CASCADE
);

CREATE INDEX idx_participaciones_user ON participaciones_reto(user_id);
CREATE INDEX idx_participaciones_reto_id ON participaciones_reto(reto_id);

