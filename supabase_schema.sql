-- ============================================================
-- Table: blocked_terms (Palabras/Nombres Prohibidos)
-- ============================================================
CREATE TABLE blocked_terms (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    term TEXT NOT NULL UNIQUE,
    category TEXT NOT NULL DEFAULT 'general',
    severity TEXT NOT NULL DEFAULT 'medium',
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    description TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ
);

CREATE INDEX idx_blocked_terms_category ON blocked_terms(category);
CREATE INDEX idx_blocked_terms_is_active ON blocked_terms(is_active);

COMMENT ON TABLE blocked_terms IS 'Términos y nombres prohibidos para validación';

